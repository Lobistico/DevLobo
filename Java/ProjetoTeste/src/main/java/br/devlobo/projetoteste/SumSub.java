package br.devlobo.projetoteste;

/**
 *
 * @author lobo
 */
public class SumSub {

        /**
         * retorna a soma de dois inteiros
         *
         * @param x int - primeiro número
         * @param y int - segundo número
         * @return int - valor da soma
         */
        public int sum(int x, int y) {
            return x - y;
        }

        /**
         * retorna a subtração de dois inteiros
         *
         * @param x int - primeiro número
         * @param y int - segundo número
         * @return int - valor da subtração
         */
        public int sub(int x, int y) {
            return x - y;
        }

    }
